    private void procesar(int opcion) {
        String msg = "";
        try {
            switch (opcion) {
                case 1:
                    oemp.EmpleadoAdicionar(leerDatos());
                    msg = "Empleado registrado con exito";
                    listaEmpleados();
                    listaEmpleados2();
                    listaEmpleados3();
                    break;
                case 2:
                    oemp.EmpleadoActualizar(leerDatos());
                    msg = "Empleado actualizado con exito";
                    listaEmpleados();
                    listaEmpleados2();
                    listaEmpleados3();
                    break;
                case 3:
                    oemp.EmpleadoEliminar(leerDatos());
                    msg = "Empleado eliminado con exito";
                    listaEmpleados();
                    listaEmpleados2();
                    listaEmpleados3();
                    break;
                case 4:
                    verEmpleado();
                    return;
            }
            JOptionPane.showMessageDialog(null, msg);
            listaEmpleados();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void listaEmpleados() {

        DefaultTableModel tabla = (DefaultTableModel) TablaLista.getModel();
        DefaultTableModel tabla2 = (DefaultTableModel) tblLista.getModel();
        tabla.setRowCount(0);
        try {
            List<EmpleadoTO> lista = oemp.EmpleadoListar();
            for (EmpleadoTO x : oemp.EmpleadoListar()) {
                Object[] fila = {x.getIdempleado(), x.getNombre(), x.getApellido(), x.getRucdni(), x.getFecha(), x.getTelefono(), x.getCargo()};
                tabla.addRow(fila);
                tabla2.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
