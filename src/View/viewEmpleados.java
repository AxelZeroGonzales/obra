    private void tblListaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblListaKeyReleased
        if ((evt.getKeyCode() == KeyEvent.VK_DOWN) || (evt.getKeyCode() == KeyEvent.VK_UP)) {
            int filaSeleccionada = this.tblLista.getSelectedRow();//Identificamos que fila ha sido seleccionada
            //cuando voy a ingresar algo que te pueda votar error
            try {
                this.txtIdEmpleado.setText(tblLista.getValueAt(filaSeleccionada, 0).toString());
                this.txtNombre.setText(tblLista.getValueAt(filaSeleccionada, 1).toString());
                this.txtApellido.setText(tblLista.getValueAt(filaSeleccionada, 2).toString());
                this.txtDNI.setText(tblLista.getValueAt(filaSeleccionada, 3).toString());
                this.txtFecha.setText(tblLista.getValueAt(filaSeleccionada, 4).toString());
                this.txtTelefono.setText(tblLista.getValueAt(filaSeleccionada, 5).toString());
                this.txtCargo.setText(tblLista.getValueAt(filaSeleccionada, 6).toString());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al leer datos de la tabla", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_tblListaKeyReleased

